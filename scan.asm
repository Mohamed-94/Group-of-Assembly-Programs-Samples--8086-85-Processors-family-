.MODEL SMALL
.STACK 100H
.DATA
STRING DB 80 DUP(0)
VOWELS DB 'AEIOU'
CONSONANTS DB 'BCDFGHJKLMNPQRSTVWXYZ'
OUT1 DB 0DH,0AH,'VOWELS= $'
OUT2 DB 'CONSONANTS= $'
VOWELCT DW 0
CONSCT DW 0
.CODE
MAIN PROC
; initialize DS
MOV AX,@DATA
MOV DS,AX
MOV ES,AX
LEA DX,STRING
CALL READ_STR
MOV SI,DI
CLD
REPEAT:
LODSB
LEA DI,VOWELS
MOV CX ,5
REPNE SCASB
JNE CK_CONST
INC VOWELCT
JMP UNTIL
CK_CONST:
LEA DI,CONSONANTS
MOV CX,21
REPNE SCASB
JNE UNTIL
INC CONSCT
UNTIL:
DEC BX
JNE REPEAT
;OUTPUT NO OF VOWELS
LEA DX,OUT1
MOV AH ,9
INT 21H
MOV AX,VOWELCT
CALL OUTDEC
;OUTPUT NO OF CONSONANTS
LEA DX,OUT2
MOV AH ,9
INT 21H
MOV AX,CONSCT
CALL OUTDEC
;EXIT TO DOS
MOV AH,4CH
INT 21H
MAIN ENDP
INCLUDE DES_NUM.ASM
END MAIN