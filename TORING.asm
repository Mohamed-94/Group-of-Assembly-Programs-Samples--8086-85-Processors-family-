.MODEL SMALL
.STACK 100H
.DATA
MES1 DB "WELCOME TO THE FIRST APP$"
NTR DB "-YES MAN-"
FIR DB 34
SEC DB 54
RES  DB ?
.CODE
MAIN PROC
MOV AX,@DATA
MOV DS,AX
LEA DX,MES1
MOV AH,9
INT 21H
MOV AH,2
MOV CX,1000
MOV DL,0
Print_Loop:
INT 21H
INC DL
DEC CX
MOV DL,'!'
MOV AH,2
INT 21H
JNZ PRINT_LOOP
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN