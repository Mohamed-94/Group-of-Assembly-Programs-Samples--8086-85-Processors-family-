TITLE DECIMAL: READ WRITE 
INCLUDE DES_NUM.ASM
.MODEL SMALL
.STACK 100H
.CODE
MAIN PROC
;INPUT A NUMBER
CALL INDEC
PUSH AX
;MOVE CURSOR TO NEXT LINE
MOV AH , 2
MOV DL , 0DH
INT 21H
MOV DL ,0AH
INT 21H
;OUTPUT A NUMBER
POP AX
CALL OUTDEC
;EXIT
MOV AH,4CH
INT 21H
MAIN ENDP

END MAIN