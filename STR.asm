.MODEL SMALL
.STACK 100H
.DATA
.CODE 
MAIN PROC
READ_STR PROC NEAR
;READS AND STORES A STRING
;INPUT: DI ODFFSET OF THE STRING
;OUTPUT: DI OFFSET OF THE STRING
; BX=NUMBER OF CHARACTERS READ
PUSH DX
PUSH DI
CLD
XOR BX , BX
MOV AH , 1
INT 21H
WHILE1:
CMP AL , 0DH
JE END_WHILE1
;IF CHARACTER IS BACHSPACE
CMP AL , 8H
JNE ELSE1
DEC DI
DEC BX
JMP READ
ELSE1:
STOSB
INC BX
READ:
INT 21H
JMP WHILE1
END_WHILE1:
POP DI
POP AX
RET
READ_STR ENDP
MOV AH,4CH
INT 21H
MAIN ENDP
END MAIN